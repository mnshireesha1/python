a = 10
b = 3
print(a + b)   # 13
print(a - b)   # 7
print(a * b)   # 30
print(a / b)   # 3.33
print(a // b)  # 3 (floor division)
print(a % b)   # 1 (modulus)
print(a ** b)  # 1000 (exponentiation)

#comparison operators
print(a > b)   # True
print(a == b)  # False

#Logical opearators
x = True
y = False
print(x and y)  # False
print(x or y)   # True
print(not x)    # False
